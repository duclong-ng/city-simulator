package com.natuan.citysimulator.model;

public class PointVelocity {
    public int vX;
    public int vY;

    public PointVelocity() {
        this.vX = 0;
        this.vY = 0;
        this.vX = 0;
        this.vY = 0;
    }

    public PointVelocity(final int vx, final int vy) {
        this.vX = 0;
        this.vY = 0;
        this.vX = vx;
        this.vY = vy;
    }
}
