package com.natuan.citysimulator.model;

public class TransPoint {
    public int x;
    public int y;

    public TransPoint() {
        this.x = 0;
        this.y = 0;
        this.x = 0;
        this.y = 0;
    }

    public TransPoint(final int x, final int y) {
        this.x = 0;
        this.y = 0;
        this.x = x;
        this.y = y;
    }
}

